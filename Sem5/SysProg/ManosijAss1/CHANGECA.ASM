;ACCEPTS CHARACTYER AND CHANGES IT'S CASE
.MODEL SMALL
.STACK 300H
.DATA
MSG DB "ENTER CHARACTER $"
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
LEA DX,MSG
MOV AH,09H ;DISPLAY ACCEPT STATEMENT
INT 21H

MOV AH,1H ;ACCEPT
INT 21H

CMP AL,'a'
JL NOT_LOWER
CMP AL,'z'
JG NOT_LOWER

SUB AL,20H

NOT_LOWER:

MOV DL,AL ;DISPLAY
MOV AH,2H
INT 21H

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN

