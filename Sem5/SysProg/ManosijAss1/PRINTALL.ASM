;PRINT ALL THE ALPHABETS FROM A TO Z
.MODEL SMALL
.STACK 300H
.DATA
MSG DB "THE CHARACTERS $"
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
LEA DX,MSG
MOV AH,09H ;DISPLAY ACCEPT STATEMENT
INT 21H

MOV AL,41H ;INITILAISE

LOOP1:
MOV DL,AL ;DISPLAY
MOV AH,2H
INT 21H

CMP AL,'Z'
JE FINISHED

ADD AL,01H

CMP AL,'Z'
JNE LOOP1


FINISHED:

MOV DL,AL ;DISPLAY
MOV AH,2H
INT 21H

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
